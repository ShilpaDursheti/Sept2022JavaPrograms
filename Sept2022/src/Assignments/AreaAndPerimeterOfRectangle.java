package Assignments;

public class AreaAndPerimeterOfRectangle {
	
	public static void main(String args[]) {
		
		
		int l = 16;
		double b = 10.23;
		double Area = l*b;
		double Perimeter = 2*(l+b);
		
		
		System.out.println("Area of the rectangle = "+ Area+ '\n');
		System.out.println("Area of the Perimeter = "+ Perimeter);
		
	}

}
