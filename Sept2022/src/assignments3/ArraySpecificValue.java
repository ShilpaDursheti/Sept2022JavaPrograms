package assignments3;

public class ArraySpecificValue {
	
	public static void main(String args[]) {
		
		int a[]= {3,5,8,7,9,2};
		
		for(int i=0; i<a.length; i++) {
			if (a[i]==7){
				System.out.print("The number 7 is found at " + "a[" + i + "] ");
				break;
				
			}
					
					
		}
		
		
	}

}
